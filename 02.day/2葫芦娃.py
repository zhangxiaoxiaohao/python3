class huluwa():
	def dawa(self):
		print("变大")
	def erwa(self):
		print("千里眼顺风耳")
	def sanwa(self):
		print("金刚不坏")
	def siwa(self):
		print("吐水")
	def wuwa(self):
		print("吐火")
	def liuwa(self):
		print("隐身")
	def qiwa(self):
		print("宝葫芦")
	def nenli(self):
		print("作用%s\t头饰%s"%(self.yong,self.tou))
hulu = huluwa()
hulu.dawa()
hulu.erwa()
hulu.sanwa()
hulu.siwa()
hulu.wuwa()
hulu.liuwa()
hulu.qiwa()
hulu.yong = "保护爷爷"
hulu.tou = "葫芦头饰"
hulu.nenli()









